                    <h3>Kuitansi (Receipts)</h3>
                    <p>
                        Kuitansi merupakan bukti transaksi yang muncul akibat terjadinya penerimaan uang sebagai alat pembayaran suatu transaksi yang dibuat oleh penerima uang.
                        Berdasarkan definisi di atas maka dapat diketahui kuitansi dibuat oleh pihak yang menerima uang, kuitansi dibuat karena telah terjadi pembayaran sesuatu misalnya pembayaran sewa gedung, pembayaran biaya iklan, dan sebagainya.
                        Kuitansi bewujud lembaran-lembaran yang terdiri dari dua bagian dan biasanya telah terjilid seperti buku, bagian yang kanan atau bagian yang akan disobek adalah bagian untuk orang yang membanyar sedangkan bagian yang kiri untuk penerima uang pembayaran.
                    </p>

                    <h3>Nota Kontan (Notes)</h3>
                    <p>
                        Nota kontan atau sering disebut "Nota" adalah bukti pembelian atau penjualan barang yang dilakukan secara tunai. Nota merupakan bukti transaksi yang sering dijumpai selain kuitansi, misalnya saja ketika kalian pergi ke jasa foto kopi untuk memfoto kopi beberapa dokumen setelah itu kalian meminta nota ke jasa foto kopi tersebut maka kalian akan diberikan sebuah nota yang menerangkan harga foto kopi dokumen kalian.
                    </p>

                    <h3>Faktur (Invoices)</h3>
                    <p>
                        Faktur merupakan bukti pembelian atau penjualan barang yang dilakukan secara kredit. Faktur dibuat rangkap tiga, yang pertama untuk bagian gudang, yang kedua bagian keuangan (yang digunakan untuk proses penagihan) dan yang ketiga untuk pembeli sebagai bukti bahwa dia mempunyai hutang.
                    </p>

                    <h3>Nota Debit</h3>
                    <p>
                        Nota debit merupakan bukti transaksi pengembalian barang yang dibuat oleh pembeli, barang dikembalikan karena tidak sesuai pesanan atau barang cacat.
                    </p>

                    <h3>Nota Kredit</h3>
                    <p>
                        Nota kredit merupakan bukti transaksi pengembalian barang yang dibuat oleh penjual, barang dikembalikan karena tidak sesuai pesanan atau barang cacat.
                    </p>
