                <h1>Laporan Keuangan</h1>
                <article>
                    Dalam menyusun laporan keuangan setidaknya ada empat proses yang harus dilalui, yaitu aktivitas pencatatan, penggolonganm, peringkasan dan pelaporan, dalam ilmu akuntansi, keempat proses tersebut merujuk pada sebuah siklus yang disebut siklus akuntansi.
                    Aktivitas pencatatan adalah peoses mencatat seluruh transaksi atau kejadian ekonomi yang terjadi di perusahaan. dalam proses ini, pencatatan dilakukan di dalam media yang disebut sebagai jurnal. di dalam jurnal tersapat dua kolom yaitu kolom debit dan kredit. dari kedua kolom tersebut, jumlah transaksi yang dicatat atau dimasukkan haruslah sama, dalam artian, jumlah transaksi di kolom debit harus sama dengan jumlah kolom kredit.
                    Selanjutnya penggolongan adalah proses pengelompokan atau menggolongkan transaksi yang mempunyai akun atau rekening yang sama ke dalam satu catatan tersendiri yang disebut sebagai buku besar.
                    Sementara peringkasan adalah proses meringkas atau memasukkan saldo - saldo dari buku besar ke dalam suatu catatan yang dimasukkan sebagai neraca lajur.
                    Terakhir adalah proses memindahkan transaksi yang ada di dalam neraca lajur ke dalam format standar akuntasi, yang berupa laporan raba/rugi, laporan perubahan modal, neraca, laporan arus kas, dan catatan atas laporan keuangan.
                </article>
                <article>
                    <h3>Laporan Laba/Rugi</h3>
                    <p>
                        Laporan laba/rugi adalah laporan yang mencatat unsur - unsur penghasilan dan beban perusahaan yang sampai akhirnya menghasilkan suatu laba atau rugi bersih.
                        Inti dari laporan rugi / laba adalah mencatat seluruh penghasilan (pemasukan) dan beban (pengeluaran) perusahaan dalam kurun waktu tertentu. unsur laba/rugi biasanya terdiri dari penghasilan, total semua penghasilan, bebean, jumlah semua beban dan hasil akhir.
                        Laba/rugi = total semua penghasilan - jumlah semua beban.
                    </p>

                    <h3>Laporan Perubahan Modal</h3>
                    <p>
                        Laporan perubahan modal adalah bagian dari laporan keuangan yang mencatat informasi tentang penyebab bertambah atau berkurangnya modal dalam kurun waktu tertentu.
                        Unsur - unsur laporan perubahan modal biasanya terdiri dari modal awal, laba/rugi bersih, prive, penambahan modal, dan hasil akhir. Perubahan modal akhir = modal awal + (laba bersih - prive).
                    </p>

                    <h3>Laporan Neraca</h3>
                    <p>
                        Neraca adalah bagian dari laporan keuangan yang mencatat informasi tentang asset, kewajiban pembayaran pada pihak yang terkait dalam proses operasional perusahaan, dan modal pada saat tertentu.
                        Unsur - unsur neraca biasanya terdiri dari aktiva (baik aktiva lancar, jangka panjang, tetap, maupun aktiva tak berwujud), kewajiban (baik kewajiban jangka panjang maupun jangka pendek), dan modal.
                    </p>

                    <h3>Laporan Arus Kas</h3>
                    <p>
                        Laporan arus kas adalah bagian dari laporan keuangan yang mencatat informasi tentang aliran kas masuk dan keluar suatu perusahaan dalam kurun waktu tertentu. Jadi laporan arus kas erat hubungannya dengan pemasukkan yang berarti penghasilan dan pengeluaran yang berarti beban. Unsur - unsur laporan arus kas biasanya terdiri dari aktiva operasi, aktiva operasi, aktiva investasi dan aktiva pembiayaan.
                    </p>

                    <h3>Catatan atas Laporan Keuangan</h3>
                    <p>
                        Catatan atas laporan keuangan adalah catatan tambahan yang ditulis setelah laporan keuangan selesai disusun. Tujuan dari catatan atas laporan keuangan ini adalah menjelaskan segala bentuk keterangan yang tidak bisa dijelaskan didalam laporan keuangan,
                    </p>
                </article>

